# count , short , sorted function , reverse , clear , copy

# count Method
fruits = ['orange','apple','pear','banana','kiwi','apple','banana']
print(fruits.count('pear'))

# Sort method
name = ['r','t','u','s','w','a']
name.sort()
print(name)

nums = ['5','8','2','7','6']
nums.sort()
print(nums)

# Sorted function
numbers = ['51','73','15','20','34','45','55','60']
print(sorted(numbers))
print(numbers)

# clear method
numbers = ['51','73','15','20','34','45','55','60']
numbers.clear()
print(numbers)

# copy method
numbers = ['51','73','15','20','34','45','55','60']
numbers_copy = numbers.copy()
print(numbers_copy)